public class P3Mult {


    public static int mult(int a,int b){
        return a*b;
    }
    public static void main(String args[]){
        int a=20;
        int b=30;
        int prod=mult(a,b);
        System.out.println("a*b= "+prod);
        prod=mult(30,40);
        System.out.println(prod);
    }
}
